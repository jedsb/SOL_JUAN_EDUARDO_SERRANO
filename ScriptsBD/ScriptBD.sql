
CREATE SEQUENCE ALUMNOS_SEQ
    START WITH 1
    INCREMENT BY 1
    NOCACHE
    NOCYCLE;
	
CREATE SEQUENCE MATRICULA_SEQ
    START WITH 1
    INCREMENT BY 1
    NOCACHE
    NOCYCLE;

CREATE SEQUENCE CURSO_SEQ
    START WITH 1
    INCREMENT BY 1
    NOCACHE
    NOCYCLE;
    
 CREATE SEQUENCE SECCIONES_SEQ
    START WITH 1
    INCREMENT BY 1
    NOCACHE
    NOCYCLE;   

CREATE TABLE ALUMNO (
  ID_ALUMNO INT NOT NULL,
  DNI VARCHAR(8) NOT NULL,  
  NOMBRES VARCHAR(150) NOT NULL,
  APELLIDOS VARCHAR(150) NOT NULL,
  ESTADO INT DEFAULT 1,
  PRIMARY KEY (ID_ALUMNO)
);

CREATE TABLE CURSO (
  ID_CURSO INT NOT NULL,
  NOMBRE_CURSO VARCHAR(200) NOT NULL,
  CREDITOS INT NOT NULL,
  ESTADO INT DEFAULT 1,
  PRIMARY KEY (ID_CURSO)
);

CREATE TABLE SECCION (
  ID_SECCION INT NOT NULL,
  NOMBRE VARCHAR(50) NOT NULL,
  ESTADO INT DEFAULT 1,
  ID_CURSO INT NOT NULL,
  PRIMARY KEY (ID_SECCION)
);


CREATE TABLE MATRICULA (
  ID_MATRICULA INT NOT NULL,
  ID_ALUMNO INT NOT NULL,
  ID_CURSO INT NOT NULL,
  ID_SECCION INT NOT NULL,
  TIPO_MATRICULA VARCHAR(10) NOT NULL,
  FECHA_MATRICULA TIMESTAMP  NOT NULL,
  FECHA_ANULACION TIMESTAMP ,
  PRIMARY KEY (ID_MATRICULA)
);

CREATE TABLE VACANTE (
 ID_VACANTE INT NOT NULL,
  ID_CURSO INT NOT NULL,
  ID_SECCION INT NOT NULL,
  VACANTES_DISPONIBLES INT NOT NULL,
  VACANTES_USADAS INT NOT NULL,
PRIMARY KEY (ID_VACANTE)
);


